package us.vistacore.asm;

public class VistaDAOException extends Exception {

    private static final long serialVersionUID = 1L;

    public VistaDAOException() {
        super();
    }

    public VistaDAOException(String message, Throwable cause) {
        super(message, cause);
    }

    public VistaDAOException(String message) {
        super(message);
    }

    public VistaDAOException(Throwable cause) {
        super(cause);
    }

}
